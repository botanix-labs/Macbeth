fn setup() -> (
    frost::Identifier,
    frost::Identifier,
    frost::Identifier,
    //
    DkgStateMachine,
    DkgStateMachine,
    DkgStateMachine,
) {
    let secp = secp256k1::Secp256k1::new();

    let alice_sec = secp256k1::SecretKey::new(&mut rand::thread_rng());
    let alice_pub = secp256k1::PublicKey::from_secret_key(&secp, &alice_sec);

    let bob_sec = secp256k1::SecretKey::new(&mut rand::thread_rng());
    let bob_pub = secp256k1::PublicKey::from_secret_key(&secp, &bob_sec);

    let eve_sec = secp256k1::SecretKey::new(&mut rand::thread_rng());
    let eve_pub = secp256k1::PublicKey::from_secret_key(&secp, &eve_sec);

    let alice_addr = frost::Identifier::derive(0u16.to_le_bytes().as_slice()).unwrap();
    let bob_addr = frost::Identifier::derive(1u16.to_le_bytes().as_slice()).unwrap();
    let eve_addr = frost::Identifier::derive(2u16.to_le_bytes().as_slice()).unwrap();

    let mut members = BTreeMap::new();
    members.insert(alice_addr, alice_pub);
    members.insert(bob_addr, bob_pub);
    members.insert(eve_addr, eve_pub);

    let config = Config {
        max_signers: 3,
        min_signers: 3,
        round1_package_timeout: Duration::from_secs(3),
        round2_package_timeout: Duration::from_secs(4),
        round3_package_timeout: Duration::from_secs(5),
    };

    let alice =
        DkgStateMachine::new(alice_addr, alice_sec, alice_addr, members.clone(), config).unwrap();

    let bob = DkgStateMachine::new(bob_addr, bob_sec, alice_addr, members.clone(), config).unwrap();

    let eve = DkgStateMachine::new(eve_addr, eve_sec, alice_addr, members.clone(), config).unwrap();

    (alice_addr, bob_addr, eve_addr, alice, bob, eve)
}

/// A helper struct to check the messages generated by the DKG state machine.
pub struct CheckedSend {
    payloads: Vec<DkgPayload>,
    sender: frost::Identifier,
    cursor: usize,
}

impl CheckedSend {
    /// Create a new `CheckedSend` instance with the messages generated by the
    /// DKG state machine.
    pub fn new(machine: &mut DkgStateMachine, now: Instant) -> Self {
        let sender = machine.frost_id();

        let mut payloads = vec![];
        while let Some(dkg) = machine.send(now) {
            payloads.push(dkg);
        }

        CheckedSend { payloads, sender, cursor: 0 }
    }
    pub fn round1(mut self, initiator: frost::Identifier, recipient: frost::Identifier) -> Self {
        let dkg = &self.payloads[self.cursor];

        let DkgMessage::Round1 { initiator: i, .. } = dkg.msg else {
            panic!("Expected round1 message");
        };

        assert_eq!(dkg.sender, self.sender);
        assert_eq!(dkg.recipient, recipient);
        assert_eq!(i, initiator.into());

        self.cursor += 1;
        self
    }
    pub fn ack_round1(
        mut self,
        initiator: frost::Identifier,
        recipient: frost::Identifier,
    ) -> Self {
        let dkg = &self.payloads[self.cursor];

        let DkgMessage::AckRound1 { initiator: i } = dkg.msg else {
            panic!("Expected ack_round1 message");
        };

        assert_eq!(dkg.sender, self.sender);
        assert_eq!(dkg.recipient, recipient);
        assert_eq!(i, initiator.into());

        self.cursor += 1;
        self
    }
    pub fn round2(
        mut self,
        initiator: frost::Identifier,
        target: frost::Identifier,
        recipient: frost::Identifier,
    ) -> Self {
        let dkg = &self.payloads[self.cursor];

        let DkgMessage::Round2 { initiator: i, target: t, .. } = dkg.msg else {
            panic!("Expected round2 message");
        };

        assert_eq!(dkg.sender, self.sender);
        assert_eq!(dkg.recipient, recipient);
        assert_eq!(i, initiator.into());
        assert_eq!(t, target.into());

        self.cursor += 1;
        self
    }
    pub fn ack_round2(
        mut self,
        initiator: frost::Identifier,
        target: frost::Identifier,
        recipient: frost::Identifier,
    ) -> Self {
        let dkg = &self.payloads[self.cursor];

        let DkgMessage::AckRound2 { initiator: i, target: t } = dkg.msg else {
            panic!("Expected ack_round2 message");
        };

        assert_eq!(dkg.sender, self.sender);
        assert_eq!(dkg.recipient, recipient);
        assert_eq!(i, initiator.into());
        assert_eq!(t, target.into());

        self.cursor += 1;
        self
    }
    pub fn round3(mut self, initiator: frost::Identifier, recipient: frost::Identifier) -> Self {
        let dkg = &self.payloads[self.cursor];

        let DkgMessage::Round3 { initiator: i, .. } = dkg.msg else {
            panic!("Expected round3 message");
        };

        assert_eq!(dkg.sender, self.sender);
        assert_eq!(dkg.recipient, recipient);
        assert_eq!(i, initiator.into());

        self.cursor += 1;
        self
    }
    pub fn ack_round3(
        mut self,
        initiator: frost::Identifier,
        recipient: frost::Identifier,
    ) -> Self {
        let dkg = &self.payloads[self.cursor];

        let DkgMessage::AckRound3 { initiator: i } = dkg.msg else {
            panic!("Expected ack_round3 message");
        };

        assert_eq!(dkg.sender, self.sender);
        assert_eq!(dkg.recipient, recipient);
        assert_eq!(i, initiator.into());

        self.cursor += 1;
        self
    }
    pub fn msgs<U>(self) -> U
    where
        U: TryFrom<Vec<DkgPayload>>,
        <U as TryFrom<Vec<DkgPayload>>>::Error: std::fmt::Debug,
    {
        if self.cursor != self.payloads.len() {
            panic!("Not all messages were checked");
        }

        self.payloads.try_into().expect("Failed to retrieve messages")
    }
}
